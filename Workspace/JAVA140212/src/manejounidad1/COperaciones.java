package manejounidad1;

public class COperaciones {
	
	public static int suma( int x, int y) {
		
		return x + y;
	} // Fin de suma
	
    public static int resta( int x, int y) {

		return x - y;
	} // Fin de resta

    public static int multiplica( int x, int y) {

		return x * y;
    } // Fin de multiplicacion
    
	public static double suma( double x, double y) {
		
		return x + y;
	} // Fin de suma
	
    public static double resta( double x, double y) {

		return x - y;
	} // Fin de resta

    public static double multiplica( double x, double y) {

		return x * y;
    } // Fin de multiplicacion

}
